
package jcf;

public class Corder {
    private int no;
    private int mcode;
    private int oprice;
    private int cprice;
    private String username;
    private int mqu;

    public void setMqu(int mqu) {
        this.mqu = mqu;
    }

    public int getMqu() {
        return mqu;
    }

    public void setNo(int no) {
        this.no = no;
    }

    public void setMcode(int mcode) {
        this.mcode = mcode;
    }

    public void setOprice(int oprice) {
        this.oprice = oprice;
    }

    public void setCprice(int cprice) {
        this.cprice = cprice;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public int getNo() {
        return no;
    }

    public int getMcode() {
        return mcode;
    }

    public int getOprice() {
        return oprice;
    }

    public int getCprice() {
        return cprice;
    }

    public String getUsername() {
        return username;
    }
    
}
